export const HERO_STICKERS: Variant[] = ["star", "heart", "bolt", "gem"];

export function PixelSticker({ variant, size = 48, className = "" }: Props) {
  const { pixels, colors } = ART[variant];
  const cell = size / 16;

  return (
    <svg
      width={size}
      height={size}
      viewBox={`0 0 ${size} ${size}`}
      className={`shrink-0 ${className}`.trim()}
      style={{ imageRendering: "pixelated" }}
      aria-hidden
    >
      {pixels.map((row, y) =>
        row.split("").map((ch, x) => {
          if (ch === "0") return null;
          const fill = colors[ch];
          if (!fill) return null;
          return <rect key={`${x}-${y}`} x={x * cell} y={y * cell} width={cell} height={cell} fill={fill} />;
        }),
      )}
    </svg>
  );
}
